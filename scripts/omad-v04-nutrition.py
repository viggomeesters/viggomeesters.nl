def add_scaled(total, values, grams):
    for key, value in values.items():
        if isinstance(value,(int,float)):
            total[key] = total.get(key,0.0) + value * grams / 100.0

def calculate_item(product):
    total={}
    for food_id, grams in product["components"]:
        add_scaled(total, USDA_SR28[str(food_id)]["nutrients"], grams)
    total.update(product["overrides"])
    return total

