def blend_profiles(chicken, salmon):
    """Daily average for the declared six-chicken/one-salmon week."""
    summary={key:round((chicken["summary"][key]*6+salmon["summary"][key])/7,1) for key in chicken["summary"]}
    salmon_rows={row["key"]:row for row in salmon["nutrients"]}
    rows=[]
    for chicken_row in chicken["nutrients"]:
        salmon_row=salmon_rows[chicken_row["key"]]
        if chicken_row["value"] is None or salmon_row["value"] is None:
            value=pct=None; status="unknown"
        else:
            value=round((chicken_row["value"]*6+salmon_row["value"])/7,2)
            pct=round(value/chicken_row["target"]*100)
            if chicken_row["is_limit"]:
                status="high" if pct>100 else ("watch" if pct>75 else "good")
            else:
                status="good" if pct>=100 else ("watch" if pct>=75 else "low")
        rows.append(chicken_row | {"value":value,"pct":pct,"status":status})
    return {
      "protein":"rotation", "name":"6 + 1 weekly average", "item_keys":["6 × chicken day","1 × salmon day"],
      "weight_g":round((chicken["weight_g"]*6+salmon["weight_g"])/7),
      "cost_eur":round((chicken["cost_eur"]*6+salmon["cost_eur"])/7,2),
      "summary":summary, "nutrients":rows
    }

